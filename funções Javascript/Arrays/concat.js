/* O método concat() retorna em um novo array a concatenação de todos os arrays 
ou valores passados como parâmetro. */

const array1 = ['1', '2', '3', '4', '5', '6']
const array2 = ['7', '8', '9', '10', '11', '12']

console.log('array1:', array1)
console.log('array2:', array2)

const array3 = array1.concat(array2)
console.log('array3 concatenado array1 + array2: ', array3)
